//using method convert string to upperCase and LowerCase
package javastring;

public class StringConversion {
    public static void main(String[] args) {
        String places="bombay , kolkata, delhi";
        String placesLowerString = places.toLowerCase();
        String placeUpperString = places.toUpperCase();
        System.out.println(placeUpperString);
        System.out.println(placesLowerString);
    }
}
